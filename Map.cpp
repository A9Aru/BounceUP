Map::Map()
{
	rock = Texture::LoadTexture("rock.png", Game::renderer);
	loadMap(lev1_map);
	src.x = src.y = 0;
	src.h = dest.h = 32;
	src.w = dest.w = 18;
	dest.x = dest.y = 0;
}

void Map::loadMap(int arr[40][40])
{
	for (int i = 0; i < 40; i++)
	{
		for (int j = 0; j < 40; j++)
		{
			map[i][j] = lev1_map[i][j];
		}
	}
}

void Map::drawMap()
{
	int type;
	for (int i = 0; i < 40; i++)
	{
		for (int j = 0; j < 40; j++)
		{
			type = map[i][j];
			dest.x = j * 32;
			dest.y = i * 18;
			switch (type)
			{
			case 1:
				Texture::draw_text(rock, src, dest);
			default:
				break;
			}
		}
	}

}